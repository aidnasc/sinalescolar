    private void atualizarTabela() {
        if (comboNiveis.getSelectedIndex() > 0) {
            ld.popularTabelaLogs(tblLogs, comboNiveis.getSelectedItem().toString());
        } else {
            ld.popularTabelaLogs(tblLogs, "");
        }
    }
    
    private void atualizarCombo() {
        ld.popularComboBoxNiveisLogs(comboNiveis);
    }
    
    private void atualizarLista() {
        if (comboNiveis.getSelectedIndex() > 0) {
            lista = ld.getAllLogsByLevel(comboNiveis.getSelectedItem().toString());
        } else {
            lista = ld.getAllLogs();
        }
    }
    
    private void limparSelecao() {
        comboNiveis.setSelectedIndex(0);
        tblLogs.clearSelection();
        txtMensagem.setText("");
    }

